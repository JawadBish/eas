      <h1 id="get-wasp">get-wasp</h1>

<p>This repo is used as a way to publish static wasp resources, primarily installer script.</p>

<p>Anything that you put here in master branch will be available under get.wasp-lang.dev/ (via Github Pages).</p>

<p>Therefore, installer.sh will be available as get.wasp-lang.dev/installer.sh .</p>

<h2 id="development">Development</h2>

<p>Test with <code class="language-plaintext highlighter-rouge">dash installer.sh</code>, to ensure it works on variety with <code class="language-plaintext highlighter-rouge">sh</code>, not just <code class="language-plaintext highlighter-rouge">bash</code> (dash is implementation of <code class="language-plaintext highlighter-rouge">sh</code>, so simpler than <code class="language-plaintext highlighter-rouge">bash</code>, and is a default shell on ubuntu).</p>

<p>Also, use shellcheck to check the code: <code class="language-plaintext highlighter-rouge">shellcheck installer.sh</code>.</p>


      
